// 5.	C++ program to check number is positive, negative or zero.

#include <bits/stdc++.h>
using namespace std;
int main()
{
    int a;
    cin>>a;
    if(a>0)cout<<"Positive"<<endl;
    else if(a<0)cout<<"Negative"<<endl;
    else cout<<"Zero"<<endl;
    return 0;
}

/*
input:
-1

output:
Negative


*/