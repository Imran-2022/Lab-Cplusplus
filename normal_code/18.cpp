// 18.	Write a C++ program to compute the quotient and remainder.  

#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n,m;
    cin>>n>>m;
    cout<<"quotient : "<<n/m<<endl;
    cout<<"remainder : "<<n%m<<endl;
    return 0;
}

/*
input:
10 3

output:
quotient : 3
remainder : 1


*/