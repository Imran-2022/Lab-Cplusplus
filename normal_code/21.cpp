// 21.	C++ Program to perform all arithmetic operations.

#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n,m;
    cin>>n>>m;
    cout<<"summation : "<<n+m<<endl;
    cout<<"multiplication : "<<n*m<<endl;
    cout<<"division : "<<n*1.0/m<<endl;
    cout<<"addition : "<<n+m<<endl;
    return 0;
}

